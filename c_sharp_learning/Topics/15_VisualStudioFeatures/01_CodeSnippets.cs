// This file demonstrates the concept: Code snippets in Visual Studio
namespace Topics.VisualStudioFeatures
{
    public class CodeSnippets
    {
        // Example: Using the 'prop' code snippet to create a property
        public int MyProperty { get; set; }

        // Example: Using the 'cw' code snippet to write to the console
        public void PrintHello()
        {
            System.Console.WriteLine("Hello from code snippet!");
        }
    }
}
