namespace Topics.Introduction_Basics
{
    public class CommonOperators
    {
        public void Demo()
        {
            int a = 5, b = 2;
            int sum = a + b;
            int diff = a - b;
            int prod = a * b;
            int quot = a / b;
            bool isEqual = a == b;
            bool isGreater = a > b;
        }
    }
}
